def add(x,y):
    result=x+y
    return result

def subtract(y,z):
    result = y-z
    return result

def multiply(a,b):
    result = a*b
    return result

def divide(c,d):
    result = c/d
    return result

def remainder(e,f):
    result = e%f
    return result

def power_of(g,h):
    result = g**h
    return result

def sum(*numbers):
    total = 0
    for number in numbers:
        total+=number
    return total    

def multiply_many(*numbers):
    product = 1
    for number in numbers:
        product*=number
    return product
















